import org.openqa.selenium.WebElement;
import org.openqa.selenium.support.PageFactory;

public class DetayliAraPage {

    AndroidDriver driver;
    public DetayliAraPage(AndroidDriver driver) {
        this.driver = driver;
        PageFactory.initElements(new AppiumFieldDecorator(driver), this);
    }

    @AndroidFindBy(xpath = "//android.view.View[contains(@content-desc, 'Otomobil')]")
    public WebElement otomobilTabButon;
}

